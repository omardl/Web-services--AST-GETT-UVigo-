

public class ServicioCalculadora {

      public int sumar (int s1, int s2) {
            return (s1 + s2);
      }

      public int restar (int min, int sus) {
            return (min - sus);
      }

      public int multiplicar (int m1, int m2) {
            return (m1*m2);
      }
 
      public float dividir(int num, int den) {
            return ((float)num/den);
      }

}